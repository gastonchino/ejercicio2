/*
Escribir un programa que realice la transformación de grados Celsius a Fahrenheit, 
para el valor 20 C y lo imprima por pantalla.
*/
// celsius_a_fahrenheit.js

// Valor en grados Celsius
const celsius = 20;

// Fórmula de conversión de Celsius a Fahrenheit: (Celsius * 9/5) + 32
const fahrenheit = (celsius * 9/5) + 32;

// Mostrar resultado por pantalla
console.log(`${celsius} grados Celsius es igual a ${fahrenheit} grados Fahrenheit.`);
