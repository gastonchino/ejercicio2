//11. crear un programa que dependiendo que numero del día de la semana es, dicho programa te devuelve el dia. ejemplo . si ingreso el 1 me tiene que devolver el día lunes.

let dia = 0; 

if (dia === 0) {
    console.log("Lunes");
} else if (dia === 1) {
    console.log("Martes");
} else if (dia === 2) {
    console.log("Miércoles");
} else if (dia === 3) {
    console.log("Jueves");
} else if (dia === 4) {
    console.log("Viernes");
} else if (dia === 5) {
    console.log("Sábado");
} else if (dia === 6) {
    console.log("Domingo");
} else {
    console.log("Número de día no válido");
}
