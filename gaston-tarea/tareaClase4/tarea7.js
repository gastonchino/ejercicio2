queDiaEs = 3;

switch (queDiaEs) {
    case 1:
        console.log("Es lunes, dia laboral")
        break;

    case 2:
        console.log("Es martes, dia laboral")
        break;

    case 3:
        console.log("Es miercoles, dia laboral")
        break;

    case 4:
        console.log("Es jueves, dia laboral")
        break;

    case 5:
        console.log("Es viernes, dia laboral")
        break;

    case 6:
        console.log("Es sabado,fin de semana")
        break;


    case 7:
        console.log("Es domingo,fin de semana ")
        break;

    default:

        console.log("El dia no es valido")
    }
